import fs from "node:fs";
import path from "node:path";

const root = process.cwd();
const fontsDir = path.join(root, "lib", "pdf", "fonts");
const publicFontsDir = path.join(root, "public", "fonts");
const outPath = path.join(root, "lib", "pdf", "font-data.generated.ts");

const MIN_FONT_BYTES = 100_000;
/** Noto Sans מלא (עברית + ספרות לטיניות) — לא תת-קבוצה Hebrew-only (~27KB) */
const FONT_URLS = {
  regular:
    "https://raw.githubusercontent.com/googlefonts/noto-fonts/main/hinted/ttf/NotoSans/NotoSans-Regular.ttf",
  bold: "https://raw.githubusercontent.com/googlefonts/noto-fonts/main/hinted/ttf/NotoSans/NotoSans-Bold.ttf",
};

const regularPath = path.join(fontsDir, "NotoSansHebrew-Regular.ttf");
const boldPath = path.join(fontsDir, "NotoSansHebrew-Bold.ttf");

async function ensureFontFile(destPath, url) {
  let buf = fs.existsSync(destPath) ? fs.readFileSync(destPath) : null;
  if (buf && buf.length >= MIN_FONT_BYTES && buf[0] === 0x00 && buf[1] === 0x01) {
    return buf;
  }
  console.log(`Downloading ${path.basename(destPath)} (${url})…`);
  const res = await fetch(url);
  if (!res.ok) {
    throw new Error(`Font download failed ${res.status}: ${url}`);
  }
  buf = Buffer.from(await res.arrayBuffer());
  if (buf.length < MIN_FONT_BYTES || buf[0] !== 0x00 || buf[1] !== 0x01) {
    throw new Error(`Invalid font file from ${url} (${buf.length} bytes)`);
  }
  fs.mkdirSync(path.dirname(destPath), { recursive: true });
  fs.writeFileSync(destPath, buf);
  return buf;
}

fs.mkdirSync(fontsDir, { recursive: true });
fs.mkdirSync(publicFontsDir, { recursive: true });

const regular = await ensureFontFile(regularPath, FONT_URLS.regular);
const bold = await ensureFontFile(boldPath, FONT_URLS.bold);
fs.copyFileSync(regularPath, path.join(publicFontsDir, "NotoSansHebrew-Regular.ttf"));
fs.copyFileSync(boldPath, path.join(publicFontsDir, "NotoSansHebrew-Bold.ttf"));

for (const [label, buf] of [
  ["Regular", regular],
  ["Bold", bold],
]) {
  if (buf.length < MIN_FONT_BYTES) {
    console.error(`${label} font too small (${buf.length} bytes).`);
    process.exit(1);
  }
}

const content = `/** Generated by scripts/embed-pdf-fonts.mjs — do not edit */
export const NOTO_HEBREW_REGULAR_BASE64 = ${JSON.stringify(regular.toString("base64"))};
export const NOTO_HEBREW_BOLD_BASE64 = ${JSON.stringify(bold.toString("base64"))};
`;

fs.writeFileSync(outPath, content);
console.log(`Wrote ${outPath} (${content.length} bytes)`);
