import fs from "node:fs";
import path from "node:path";

const root = process.cwd();
const fontsDir = path.join(root, "lib", "pdf", "fonts");
const outPath = path.join(root, "lib", "pdf", "font-data.generated.ts");

const regularPath = path.join(fontsDir, "NotoSansHebrew-Regular.ttf");
const boldPath = path.join(fontsDir, "NotoSansHebrew-Bold.ttf");

for (const p of [regularPath, boldPath]) {
  if (!fs.existsSync(p)) {
    console.error(`Missing font: ${p}`);
    process.exit(1);
  }
}

const regular = fs.readFileSync(regularPath);
const bold = fs.readFileSync(boldPath);

const content = `/** Generated by scripts/embed-pdf-fonts.mjs — do not edit */
export const NOTO_HEBREW_REGULAR_BASE64 = ${JSON.stringify(regular.toString("base64"))};
export const NOTO_HEBREW_BOLD_BASE64 = ${JSON.stringify(bold.toString("base64"))};
`;

fs.writeFileSync(outPath, content);
console.log(`Wrote ${outPath} (${content.length} bytes)`);
